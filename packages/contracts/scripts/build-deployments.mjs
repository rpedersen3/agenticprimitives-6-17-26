#!/usr/bin/env node
/**
 * build-deployments.mjs — R7.3.
 *
 * Reads every committed `deployments-<network>.json` next to package.json
 * and emits:
 *   - dist/deployments/<network>.js  (`export const CONTRACTS = {...}`)
 *   - dist/deployments/<network>.d.ts (typed against viem's `Address`)
 *   - dist/deployments/index.js + .d.ts (`getDeployments(network)` helper
 *     + per-network re-exports)
 *
 * The point: downstream consumers (demo-sso, demo-sso-next, demo-org,
 * third-party integrators) import the same single source of truth instead
 * of each hardcoding the address table in their own `chain.ts`. When the
 * contracts package redeploys, ONE commit (the refreshed JSON) propagates
 * to every consumer at install / rebuild time.
 *
 * Why a build step (and not just `with { type: 'json' }`):
 *   - Vite + Next.js + tsx + Hardhat all support JSON imports with
 *     SUBTLY different incantations, and the runtime field-typing
 *     is `string` instead of the branded `Address` — every consumer
 *     then re-asserts `as Address` site-by-site, recreating the very
 *     drift we're closing here. The generated .d.ts gives them
 *     typed addresses without ceremony.
 */
import { readdirSync, readFileSync, writeFileSync, mkdirSync, existsSync } from 'node:fs';
import { join } from 'node:path';

const ROOT = process.cwd();
const DIST = join(ROOT, 'dist', 'deployments');

mkdirSync(DIST, { recursive: true });

const pattern = /^deployments-([a-z0-9-]+)\.json$/;
const networks = [];

for (const entry of readdirSync(ROOT)) {
  const m = pattern.exec(entry);
  if (!m) continue;
  const network = m[1];
  // Skip the local anvil file by default — its addresses are
  // developer-machine-specific. Operators can opt in by setting
  // INCLUDE_ANVIL=1 (the file is still gitignored).
  if (network === 'anvil' && process.env.INCLUDE_ANVIL !== '1') continue;
  const data = JSON.parse(readFileSync(join(ROOT, entry), 'utf8'));
  networks.push({ network, data });
}

if (networks.length === 0) {
  console.error('[build-deployments] no deployments-*.json found in', ROOT);
  process.exit(1);
}

function jsForNetwork(network, data) {
  return [
    `// Generated by scripts/build-deployments.mjs — do not edit by hand.`,
    `// Source: deployments-${network}.json`,
    `export const CONTRACTS = ${JSON.stringify(data, null, 2)};`,
    `export default CONTRACTS;`,
    '',
  ].join('\n');
}

function dtsForNetwork(network, data) {
  const addressKeys = Object.entries(data)
    .filter(([, v]) => typeof v === 'string' && /^0x[0-9a-fA-F]{40}$/.test(v))
    .map(([k]) => `  readonly ${JSON.stringify(k)}: Address;`);
  const otherKeys = Object.entries(data)
    .filter(([, v]) => !(typeof v === 'string' && /^0x[0-9a-fA-F]{40}$/.test(v)))
    .map(([k, v]) => `  readonly ${JSON.stringify(k)}: ${typeof v === 'number' ? 'number' : 'string'};`);
  const fields = [...addressKeys, ...otherKeys];
  return [
    `// Generated by scripts/build-deployments.mjs — do not edit by hand.`,
    `// Source: deployments-${network}.json`,
    `import type { Address } from 'viem';`,
    `export interface Deployments {`,
    ...fields,
    `}`,
    `export declare const CONTRACTS: Deployments;`,
    `export default CONTRACTS;`,
    '',
  ].join('\n');
}

// Per-network files.
for (const { network, data } of networks) {
  writeFileSync(join(DIST, `${network}.js`), jsForNetwork(network, data));
  writeFileSync(join(DIST, `${network}.d.ts`), dtsForNetwork(network, data));
}

// Index aggregator: re-export named CONTRACTS_<network> for callers who
// want one tree-shakable import, plus a `getDeployments` helper for
// runtime selection.
const indexJsLines = [
  `// Generated by scripts/build-deployments.mjs — do not edit by hand.`,
];
const indexDtsLines = [
  `// Generated by scripts/build-deployments.mjs — do not edit by hand.`,
  `import type { Address } from 'viem';`,
  `import type { Deployments } from './${networks[0].network}';`,
  `export type { Deployments };`,
];

for (const { network } of networks) {
  const safeName = network.replace(/-/g, '_').toUpperCase();
  indexJsLines.push(
    `import { CONTRACTS as ${safeName} } from './${network}.js';`,
  );
  indexJsLines.push(`export { ${safeName} };`);
  indexDtsLines.push(`export declare const ${safeName}: Deployments;`);
}

const networkUnion = networks.map((n) => `'${n.network}'`).join(' | ');
indexJsLines.push('');
indexJsLines.push(`const TABLE = {`);
for (const { network } of networks) {
  const safeName = network.replace(/-/g, '_').toUpperCase();
  indexJsLines.push(`  ${JSON.stringify(network)}: ${safeName},`);
}
indexJsLines.push(`};`);
indexJsLines.push('');
indexJsLines.push(`export function getDeployments(network) {`);
indexJsLines.push(`  const c = TABLE[network];`);
indexJsLines.push(`  if (!c) throw new Error('[@agenticprimitives/contracts] no deployments for network ' + network);`);
indexJsLines.push(`  return c;`);
indexJsLines.push(`}`);
indexJsLines.push('');

indexDtsLines.push('');
indexDtsLines.push(`export type Network = ${networkUnion};`);
indexDtsLines.push(`export declare function getDeployments(network: Network): Deployments;`);
indexDtsLines.push('');

writeFileSync(join(DIST, 'index.js'), indexJsLines.join('\n'));
writeFileSync(join(DIST, 'index.d.ts'), indexDtsLines.join('\n'));

console.log(
  `[build-deployments] wrote ${networks.length} network(s) to dist/deployments/:`,
  networks.map((n) => n.network).join(', '),
);
